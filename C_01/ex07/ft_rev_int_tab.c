void	ft_rev_int_tab(int *tab, int size)
{
	int		tmp;
	int		i;

	i = 0;
	while (i < --size)
	{
		tmp = tab[i];
		tab[i] = tab[size];
		tab[size] = tmp;
		i++;
	}
}
