int		ft_iterative_factorial(int nb)
{
	int		i;
	int		ret;

	if (nb < 0)
		return (0);
	i = 2;
	ret = 1;
	while (i < nb + 1)
	{
		ret *= i;
		i++;
	}
	return (ret);
}
