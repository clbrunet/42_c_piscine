int		addition(int a, int b)
{
	return (a + b);
}

int		substraction(int a, int b)
{
	return (a - b);
}

int		division(int a, int b)
{
	return (a / b);
}

int		multiplication(int a, int b)
{
	return (a * b);
}

int		modulo(int a, int b)
{
	return (a % b);
}
